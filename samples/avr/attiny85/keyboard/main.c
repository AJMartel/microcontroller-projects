/* ------------------------------------------------------------------------- */
/* --------------------------------- main ---------------------------------- */
/* ------------------------------------------------------------------------- */

int main(void)
{
    odDebugInit();
    usbDeviceDisconnect();
    _delay_ms(300);
    usbDeviceConnect();
    wdt_enable(WDTO_1S);
    usbInit();
    sei();
    
    while(1){
        wdt_reset();
        usbPoll();
        if(usbInterruptIsReady()){
		    reportBuffer[0] = 0;    /* no modifiers */
		    reportBuffer[1] = 0x2C;	//Always send space

            usbSetInterrupt(reportBuffer, sizeof(reportBuffer));
        }
    }
    return 0;
}
